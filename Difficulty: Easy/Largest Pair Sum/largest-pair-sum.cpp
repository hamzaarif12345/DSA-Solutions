class Solution {
  public:
    int pairsum(vector<int> &arr) {
        // code here
        int m1=-1,m2=-1;
        for(auto a: arr){
            if(a > m1) {
                m2=m1;
                m1=a;
            }
            else if(a > m2){
                m2=a;
            }
        }return m2+m1;
        
    }
};


