class Solution {
  public:
    vector<vector<string>> anagrams(vector<string>& arr) {
        // code here
        vector<vector<string>> ans;
        unordered_map<string, vector<string>> m;
        for(auto a: arr){
            string key=a;
            sort(key.begin(),key.end());
            m[key].push_back(a);
        }
        vector<string> v;
        for(auto a : m){
            v.push_back(a.first);
        }
        sort(v.begin(),v.end());
        for(auto a:v){
            ans.push_back(m[a]);
        }return ans;
    }
};

